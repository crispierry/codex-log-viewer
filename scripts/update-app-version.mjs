import { readFile, writeFile } from "node:fs/promises";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "..");
const versionPath = resolve(repoRoot, "app-version.json");
const packagePath = resolve(repoRoot, "package.json");
const packageLockPath = resolve(repoRoot, "package-lock.json");
const swiftVersionPath = resolve(repoRoot, "apps/macos/Sources/CodexLogViewerMac/AppVersion.swift");
const args = new Set(process.argv.slice(2));

const version = await readVersion();
if (args.has("--bump-minor") || args.has("--bump-pr")) {
  version.minor += 1;
  version.patch = 0;
}
if (args.has("--bump-patch") || args.has("--bump-commit") || args.has("--bump-build")) {
  version.patch += 1;
}

validateVersion(version);
await writeVersion(version);
await writePackageVersion(version);
await writePackageLockVersion(version);
await writeSwiftVersion(version);

const formatted = formatVersion(version);
if (args.has("--json")) {
  process.stdout.write(`${JSON.stringify(formatted)}\n`);
} else {
  process.stdout.write(`App version ${formatted.displayVersion}\n`);
}

async function readVersion() {
  const raw = JSON.parse(await readFile(versionPath, "utf8"));
  const patchValue = raw.patch ?? raw.build;
  return {
    major: numericPart(raw.major, "major"),
    minor: numericPart(raw.minor, "minor"),
    patch: numericPart(patchValue, raw.patch === undefined ? "patch or build" : "patch")
  };
}

function numericPart(value, key) {
  if (!Number.isInteger(value) || value < 0) {
    throw new Error(`app-version.json ${key} must be a non-negative integer.`);
  }
  return value;
}

function validateVersion(version) {
  for (const key of ["major", "minor", "patch"]) {
    numericPart(version[key], key);
  }
}

async function writeVersion(version) {
  await writeFile(versionPath, `${JSON.stringify(version, null, 2)}\n`);
}

async function writePackageVersion(version) {
  const packageJson = JSON.parse(await readFile(packagePath, "utf8"));
  packageJson.version = formatVersion(version).marketingVersion;
  await writeFile(packagePath, `${JSON.stringify(packageJson, null, 2)}\n`);
}

async function writePackageLockVersion(version) {
  const packageLock = JSON.parse(await readFile(packageLockPath, "utf8"));
  const formattedVersion = formatVersion(version).marketingVersion;
  packageLock.version = formattedVersion;
  if (packageLock.packages?.[""]) {
    packageLock.packages[""].version = formattedVersion;
  }
  await writeFile(packageLockPath, `${JSON.stringify(packageLock, null, 2)}\n`);
}

async function writeSwiftVersion(version) {
  const formatted = formatVersion(version);
  const source = `// Generated by scripts/update-app-version.mjs.
// Use npm run version:pr for PR minor bumps and npm run version:commit for commit patch bumps.

enum AppVersion {
  static let major = ${version.major}
  static let minor = ${version.minor}
  static let patch = ${version.patch}
  static let marketingVersion = "${formatted.marketingVersion}"
  static let bundleVersion = "${formatted.bundleVersion}"
  static let displayVersion = "${formatted.displayVersion}"
}
`;
  await writeFile(swiftVersionPath, source);
}

function formatVersion(version) {
  const marketingVersion = `${version.major}.${version.minor}.${version.patch}`;
  const bundleVersion = marketingVersion;
  return {
    ...version,
    marketingVersion,
    bundleVersion,
    displayVersion: marketingVersion
  };
}
